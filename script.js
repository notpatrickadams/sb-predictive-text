function search(str) {
	return fruit.filter(item => {
		return (item.toLowerCase().includes(str.toLowerCase()));
	});
}

function searchHandler(e) {
	showSuggestions(search(e.target.value));
}

function showSuggestions(results) {
	suggestions.innerHTML = '';
	let suggestionArr = results.map(item => {
		const listItem = document.createElement('li');
		listItem.textContent = item;
		return listItem;
	});
	suggestionArr.forEach(element => {
		suggestions.appendChild(element);
	});
}

function useSuggestion(e) {
	input.value = e.target.textContent;
	suggestions.innerHTML = '';
}

input.addEventListener('keyup', searchHandler);
suggestions.addEventListener('click', useSuggestion);